/**
 * Host-side renderer signature. Every widget impl provides a `value` of
 * this type — a Composable that paints itself into the host's Compose
 * tree, given an incoming Modifier from the parent.
 *
 * The `WidgetSystem` Keliver hands to `TreehouseContent` is parameterized
 * over this signature; that's how the host plugs in its rendering layer
 * without Keliver having to know about Compose.
 */
public typealias CmpRender = @Composable (ComposeModifier) -> Unit

/**
 * Binds each codegen'd widget interface in the schema to a concrete
 * Compose impl. `SampleSchemaWidgetFactory` is generated by
 * `:shared-widget` from the `@Schema` declaration — adding a new widget
 * there means adding a new `override fun Foo()` here plus a `CmpFoo` class.
 */
public object CmpWidgetFactory : SampleSchemaWidgetFactory<CmpRender> {
  override fun Box(): Box<CmpRender> = CmpBox()
  override fun Text(): Text<CmpRender> = CmpText()
  override fun Column(): Column<CmpRender> = CmpColumn()
  override fun Row(): Row<CmpRender> = CmpRow()
  override fun Button(): Button<CmpRender> = CmpButton()
  override fun Spacer(): Spacer<CmpRender> = CmpSpacer()
}

/**
 * `Box` impl. A single `Children(1)` slot in the schema becomes a
 * `Widget.Children<CmpRender>` here — Keliver injects the host's
 * `ComposeWidgetChildren` so the guest's child widgets render inside the
 * Compose Box layout.
 */
private class CmpBox : Box<CmpRender> {
  override val children: Widget.Children<CmpRender> = ComposeWidgetChildren()
  override var modifier: dev.keliver.Modifier = dev.keliver.Modifier

  override val value: CmpRender = { incoming ->
    ComposeBox(modifier = incoming) {
      (children as ComposeWidgetChildren).Render()
    }
  }
}

/** `Column` impl — renders its children slot in a Compose Column. */
private class CmpColumn : Column<CmpRender> {
  override val children: Widget.Children<CmpRender> = ComposeWidgetChildren()
  override var modifier: dev.keliver.Modifier = dev.keliver.Modifier

  override val value: CmpRender = { incoming ->
    ComposeColumn(modifier = incoming) {
      (children as ComposeWidgetChildren).Render()
    }
  }
}

/** `Row` impl — renders its children slot in a Compose Row. */
private class CmpRow : Row<CmpRender> {
  override val children: Widget.Children<CmpRender> = ComposeWidgetChildren()
  override var modifier: dev.keliver.Modifier = dev.keliver.Modifier

