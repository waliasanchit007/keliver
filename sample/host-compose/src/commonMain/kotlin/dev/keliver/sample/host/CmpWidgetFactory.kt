}

/**
 * `Text` impl. The `@Property(1) text` from the schema becomes the
 * `text(value)` setter below. `mutableStateOf` makes recomposition
 * automatic when the guest updates the value.
 */
private class CmpText : Text<CmpRender> {
  private var textValue by mutableStateOf("")
  override var modifier: dev.keliver.Modifier = dev.keliver.Modifier

  override val value: CmpRender = { incoming ->
    ComposeText(text = textValue, modifier = incoming)
  }

  override fun text(text: String) {
    textValue = text
  }
}

/**
 * `Button` impl. `text` is a normal property; `onClick` is an event —
 * the host invokes the stored lambda on tap, and Keliver routes that call
 * back across the Zipline bridge so the guest's `onClick` actually runs.
 */
private class CmpButton : Button<CmpRender> {
  private var textValue by mutableStateOf("")
  private var onClickValue by mutableStateOf<(() -> Unit)?>(null)
  override var modifier: dev.keliver.Modifier = dev.keliver.Modifier

  override val value: CmpRender = { incoming ->
    ComposeButton(onClick = { onClickValue?.invoke() }, modifier = incoming) {
      ComposeText(text = textValue)
    }
  }

  override fun text(text: String) {
    textValue = text
  }

  override fun onClick(onClick: (() -> Unit)?) {
    onClickValue = onClick
  }
}

/** `Spacer` impl — a fixed-height gap. `height` is in dp. */
private class CmpSpacer : Spacer<CmpRender> {
  private var heightValue by mutableStateOf(0)
  override var modifier: dev.keliver.Modifier = dev.keliver.Modifier

  override val value: CmpRender = { incoming ->
    ComposeSpacer(modifier = incoming.then(ComposeModifier.height(heightValue.dp)))
  }

  override fun height(height: Int) {
    heightValue = height
  }
}
