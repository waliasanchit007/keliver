package dev.keliver.portal.codegen

private fun literalExpr(p: MappedProp): String {
  val v = "node.props[\"${p.name}\"]"
  return when (p.kind) {
    MappedKind.TEXT -> "fmtString($v ?: \"\")"
    MappedKind.INT -> "fmtInt($v)"
    MappedKind.BOOL -> "fmtBool($v)"
    MappedKind.DOUBLE -> "fmtDouble($v)"
    MappedKind.FLOAT -> "fmtFloat($v)"
    MappedKind.INT_LIST -> "fmtIntList($v)"
    MappedKind.FLOAT_LIST -> "fmtFloatList($v)"
    MappedKind.STRING_LIST -> "fmtStringList($v)"
    MappedKind.DP -> "fmtDp($v)"
    MappedKind.CONSTRAINT -> "fmtConstraint($v)"
    MappedKind.CROSS_AXIS -> "fmtCrossAxis($v)"
    MappedKind.MAIN_AXIS -> "fmtMainAxis($v)"
    MappedKind.OVERFLOW -> "fmtOverflow($v)"
  }
}

/** Support imports the EXPORTED code needs when a widget with these kinds is used. */
private fun supportImports(w: WidgetPlan.Include): List<String> = buildList {
  if (w.props.any { it.kind == MappedKind.DP }) add("dev.keliver.ui.Dp")
  if (w.props.any { it.kind == MappedKind.CONSTRAINT }) add("dev.keliver.layout.api.Constraint")
  if (w.props.any { it.kind == MappedKind.CROSS_AXIS }) add("dev.keliver.layout.api.CrossAxisAlignment")
  if (w.props.any { it.kind == MappedKind.MAIN_AXIS }) add("dev.keliver.layout.api.MainAxisAlignment")
  if (w.props.any { it.kind == MappedKind.OVERFLOW }) add("dev.keliver.layout.api.Overflow")
}

/** The exported-literal expression for a modifier prop at tree key [key]. */
private fun modLiteralExpr(kind: MappedKind, key: String): String {
  val v = "node.props[\"$key\"]"
  return when (kind) {
    MappedKind.TEXT -> "fmtString($v ?: \"\")"
    MappedKind.INT -> "fmtInt($v)"
    MappedKind.BOOL -> "fmtBool($v)"
    MappedKind.DOUBLE -> "fmtDouble($v)"
    MappedKind.FLOAT -> "fmtFloat($v)"
    MappedKind.INT_LIST -> "fmtIntList($v)"
    MappedKind.FLOAT_LIST -> "fmtFloatList($v)"
    MappedKind.STRING_LIST -> "fmtStringList($v)"
    MappedKind.DP -> "fmtDp($v)"
    MappedKind.CONSTRAINT -> "fmtConstraint($v)"
    MappedKind.CROSS_AXIS -> "fmtCrossAxis($v)"
    MappedKind.MAIN_AXIS -> "fmtMainAxis($v)"
    MappedKind.OVERFLOW -> "fmtOverflow($v)"
  }
}

fun emitExporter(widgets: List<WidgetPlan.Include>, modifiers: List<ModPlan> = emptyList()): String = buildString {
  val sorted = widgets.sortedBy { it.name }
  val mods = modifiers.sortedBy { it.name }
  appendLine("// GENERATED by :portal-schema-codegen — do not edit. Run ./gradlew :portal-schema-codegen:generatePortalCode")
  appendLine("package dev.keliver.portal")
  appendLine()
  appendLine("val composableImport: Map<String, String> = mapOf(")
  for (w in sorted) appendLine("  \"${w.name}\" to \"${w.composePackage}.${w.name}\",")
  appendLine(")")
  appendLine()
  appendLine("val supportImports: Map<String, List<String>> = mapOf(")
  for (w in sorted) {
    val sup = supportImports(w)
    if (sup.isNotEmpty()) appendLine("  \"${w.name}\" to listOf(${sup.joinToString(", ") { "\"$it\"" }}),")
  }
  appendLine(")")
  appendLine()
  appendLine("/** modifier name -> the import its extension function needs in exported code. */")
  appendLine("val modifierImport: Map<String, String> = mapOf(")
  for (m in mods) appendLine("  \"${m.name}\" to \"${m.composePackage}.${m.extensionName}\",")
  appendLine(")")
  appendLine()
  appendLine("/** \"Widget.event\" -> the single param's Kotlin type (typed action contracts, P2). */")
  appendLine("val eventParamType: Map<String, String> = mapOf(")
  for (w in sorted) {
    for (e in w.events) {
      if (e.paramCount == 1 && e.paramType != null) appendLine("  \"${w.name}.${e.name}\" to \"${e.paramType}\",")
    }
  }
  appendLine(")")
  appendLine()
  appendLine(
    """
    |private fun collectTypes(node: WidgetNode, out: MutableSet<String>) {
    |  out += node.type
    |  node.children.forEach { collectTypes(it, out) }
    |}
    |
    |private fun collectModifierNames(node: WidgetNode, out: MutableSet<String>) {
    |  for (key in node.props.keys) {
    |    if (key.startsWith("mod.")) out += key.removePrefix("mod.").substringBefore('.')
    |  }
    |  node.children.forEach { collectModifierNames(it, out) }
    |}
    |
    |// P2: single-arg actions. arg=="it" -> the event payload; "item.field" -> item-scoped data.
    |private fun fmtAction(a: Action, paramCount: Int): String {
    |  val underscores = List(paramCount) { "_" }.joinToString(", ")
    |  return when {
    |    paramCount == 0 -> "{ b.${'$'}{a.name}(${'$'}{a.arg ?: ""}) }"
    |    a.arg == "it" && paramCount == 1 -> "{ b.${'$'}{a.name}(it) }"
    |    a.arg != null -> "{ ${'$'}underscores -> b.${'$'}{a.name}(${'$'}{a.arg}) }"
    |    else -> "{ ${'$'}underscores -> b.${'$'}{a.name}() }"
    |  }
    |}
    |
    |// M5/P1-B: logic nodes contribute typed contract fields. Repeat with per-item
    |// binds (item.title) generates List<ItemType> + a typed item interface.
    |// P2: item-scoped Action args (item.id) contribute item fields too.
    |private fun itemFieldsOf(node: WidgetNode, itemVar: String, iface: LinkedHashMap<String, String>) {
    |  if (node.type != "Repeat") { // a nested Repeat opens its own item scope
    |    for ((prop, v) in node.props) {
    |      if (v is Bind && v.field.startsWith("${'$'}itemVar.")) {
    |        val sub = v.field.substringAfter('.')
    |        iface[sub] = widgetSpec(node.type)?.props?.firstOrNull { it.name == prop }?.kind?.let { kotlinTypeOf(it) } ?: "String"
    |      }
    |      val arg = (v as? Action)?.arg
    |      if (arg != null && arg.startsWith("${'$'}itemVar.")) {
    |        val sub = arg.substringAfter('.')
    |        if (sub !in iface) iface[sub] = "String"
    |      }
    |    }
    |    node.children.forEach { itemFieldsOf(it, itemVar, iface) }
    |  }
    |}
    |private fun collectLogicFields(node: WidgetNode, out: LinkedHashMap<String, String>, ifaces: LinkedHashMap<String, LinkedHashMap<String, String>>) {
    |  when (node.type) {
    |    "Condition" -> (node.props["field"] as? String)?.takeIf { it.isNotBlank() }?.let { out[it] = "Boolean" }
    |    "Repeat" -> {
    |      val items = (node.props["items"] as? String)?.takeIf { it.isNotBlank() }
    |      val itemVar = (node.props["item"] as? String)?.takeIf { it.isNotBlank() } ?: "item"
    |      if (items != null) {
    |        val iface = LinkedHashMap<String, String>()
    |        node.children.forEach { itemFieldsOf(it, itemVar, iface) }
    |        if (iface.isNotEmpty()) {
    |          val typeName = itemVar.replaceFirstChar { it.uppercase() }
    |          out[items] = "List<${'$'}typeName>"
    |          ifaces[typeName] = iface
    |        } else {
    |          out[items] = "List<String>"
    |        }
    |      }
    |    }
    |  }
    |  node.children.forEach { collectLogicFields(it, out, ifaces) }
    |}
    |
    |// P1-B: a dotted Bind field ("item.title") is item-scoped -> emit as-is;
    |// a bare field ("title") is a screen binding -> b.title.
    |private fun bindRef(field: String): String = if ('.' in field) field else "b.${'$'}field"
    |private fun fmtString(v: Any?): String = if (v is Bind) bindRef(v.field) else "\"" + v.toString().replace("\\", "\\\\").replace("\"", "\\\"") + "\""
    |private fun fmtInt(v: Any?): String = if (v is Bind) bindRef(v.field) else ((v as? Int) ?: 0).toString()
    |private fun fmtBool(v: Any?): String = if (v is Bind) bindRef(v.field) else ((v as? Boolean) ?: false).toString()
    |private fun fmtDouble(v: Any?): String = if (v is Bind) bindRef(v.field) else ((v as? Double) ?: (v as? Int)?.toDouble() ?: 0.0).toString()
    |private fun fmtFloat(v: Any?): String = "${'$'}{fmtDouble(v)}f"
    |private fun fmtDp(v: Any?): String = "Dp(${'$'}{fmtDouble(v)})"
    |private fun fmtIntList(v: Any?): String = "listOf(" + ((v as? List<*>)?.joinToString(", ") ?: "") + ")"
    |private fun fmtStringList(v: Any?): String =
    |  "listOf(" + ((v as? List<*>)?.joinToString(", ") { "\"" + it.toString().replace("\\", "\\\\").replace("\"", "\\\"") + "\"" } ?: "") + ")"
    |private fun fmtFloatList(v: Any?): String = "listOf(" + ((v as? List<*>)?.joinToString(", ") { "${'$'}{it}f" } ?: "") + ")"
    |private fun fmtConstraint(v: Any?): String = if (((v as? Int) ?: 0) == 1) "Constraint.Fill" else "Constraint.Wrap"
    |private fun fmtCrossAxis(v: Any?): String = when ((v as? Int) ?: 0) {
    |  1 -> "CrossAxisAlignment.Center"
    |  2 -> "CrossAxisAlignment.End"
    |  3 -> "CrossAxisAlignment.Stretch"
    |  else -> "CrossAxisAlignment.Start"
    |}
    |private fun fmtOverflow(v: Any?): String = if (((v as? Int) ?: 0) == 1) "Overflow.Scroll" else "Overflow.Clip"
    |private fun fmtMainAxis(v: Any?): String = when ((v as? Int) ?: 0) {
    |  1 -> "MainAxisAlignment.Center"
    |  2 -> "MainAxisAlignment.End"
    |  3 -> "MainAxisAlignment.SpaceBetween"
    |  4 -> "MainAxisAlignment.SpaceAround"
    |  5 -> "MainAxisAlignment.SpaceEvenly"
    |  else -> "MainAxisAlignment.Start"
    |}
    """.trimMargin(),
  )
  appendLine()
  appendLine("fun exportKotlin(tree: WidgetNode, functionName: String = \"ExportedScreen\"): String {")
  appendLine("  val used = mutableSetOf<String>()")
  appendLine("  collectTypes(tree, used)")
  appendLine("  val usedMods = mutableSetOf<String>()")
  appendLine("  collectModifierNames(tree, usedMods)")
  appendLine("  val contract = collectContract(tree)")
  appendLine("  val logicFields = LinkedHashMap<String, String>()")
  appendLine("  val itemIfaces = LinkedHashMap<String, LinkedHashMap<String, String>>()")
  appendLine("  collectLogicFields(tree, logicFields, itemIfaces)")
  appendLine("  contract.fields.keys.forEach { logicFields.remove(it) }") // widget binds win the type
  appendLine("  val hasContract = !contract.isEmpty || logicFields.isNotEmpty()")
  appendLine("  val sb = StringBuilder()")
  appendLine("  sb.append(\"import androidx.compose.runtime.Composable\\n\")")
  appendLine("  val modImports = usedMods.mapNotNull { modifierImport[it] } + (if (usedMods.isNotEmpty()) listOf(\"dev.keliver.Modifier\") else emptyList())")
  appendLine("  val imports = (used.mapNotNull { composableImport[it] } + used.flatMap { supportImports[it] ?: emptyList() } + modImports).distinct().sorted()")
  appendLine("  imports.forEach { sb.append(\"import \$it\\n\") }")
  appendLine("  val param = if (!hasContract) \"\" else \"b: \${functionName}Bindings\"")
  appendLine("  sb.append(\"\\n@Composable\\nfun \$functionName(\$param) {\\n\")")
  appendLine("  emitNode(sb, tree, \"  \")")
  appendLine("  sb.append(\"}\\n\")")
  appendLine("  if (hasContract) {")
  appendLine("    itemIfaces.forEach { (name, fields) ->")
  appendLine("      sb.append(\"\\ninterface \$name {\\n\")")
  appendLine("      fields.forEach { (f, t) -> sb.append(\"  val \$f: \$t\\n\") }")
  appendLine("      sb.append(\"}\\n\")")
  appendLine("    }")
  appendLine("    sb.append(\"\\n/** The round-trip boundary: implement this by hand; the portal never touches it. */\\n\")")
  appendLine("    sb.append(\"interface \${functionName}Bindings {\\n\")")
  appendLine("    contract.fields.forEach { (f, k) -> sb.append(\"  val \$f: \${kotlinTypeOf(k)}\\n\") }")
  appendLine("    logicFields.forEach { (f, t) -> sb.append(\"  val \$f: \$t\\n\") }")
  appendLine("    contract.actions.forEach { a ->")
  appendLine("      val p = contract.actionParams[a]")
  appendLine("      sb.append(if (p != null) \"  fun \$a(value: \$p)\\n\" else \"  fun \$a()\\n\")")
  appendLine("    }")
  appendLine("    sb.append(\"}\\n\")")
  appendLine("  }")
  appendLine("  return sb.toString()")
  appendLine("}")
  appendLine()
  if (mods.isNotEmpty()) {
    appendLine("/** The exported `modifier = Modifier.x().y()` chain, or null when the node has no mod props. */")
    appendLine("private fun modifierExpr(node: WidgetNode): String? {")
    appendLine("  val parts = mutableListOf<String>()")
    for (mod in mods) {
      if (mod.props.isEmpty()) {
        appendLine("  if (node.props[\"mod.${mod.name}\"] == true) parts += \"${mod.extensionName}()\"")
      } else {
        val presence = mod.props.joinToString(" || ") { "\"mod.${mod.name}.${it.name}\" in node.props" }
        val args = mod.props.joinToString(" + \", \" + ") { p -> modLiteralExpr(p.kind, "mod.${mod.name}.${p.name}") }
        appendLine("  if ($presence) parts += \"${mod.extensionName}(\" + $args + \")\"")
      }
    }
    appendLine("  return if (parts.isEmpty()) null else \"Modifier.\" + parts.joinToString(\".\")")
    appendLine("}")
    appendLine()
  }
  appendLine("private fun emitNode(sb: StringBuilder, node: WidgetNode, indent: String) {")
  appendLine("  when (node.type) {")
  // M5: logic nodes export as real control flow, not composable calls.
  appendLine("    \"Condition\" -> {")
  appendLine("      val cond = (node.props[\"field\"] as? String) ?: \"cond\"")
  appendLine("      sb.append(\"\${indent}if (b.\$cond) {\\n\")")
  appendLine("      node.children.forEach { emitNode(sb, it, \"\$indent  \") }")
  appendLine("      sb.append(\"\$indent}\\n\")")
  appendLine("    }")
  appendLine("    \"Repeat\" -> {")
  appendLine("      val item = (node.props[\"item\"] as? String) ?: \"item\"")
  appendLine("      val items = (node.props[\"items\"] as? String) ?: \"items\"")
  appendLine("      sb.append(\"\${indent}b.\$items.forEach { \$item ->\\n\")")
  appendLine("      node.children.forEach { emitNode(sb, it, \"\$indent  \") }")
  appendLine("      sb.append(\"\$indent}\\n\")")
  appendLine("    }")
  for (w in sorted) {
    appendLine("    \"${w.name}\" -> {")
    appendLine("      sb.append(\"\${indent}${w.name}(\\n\")")
    if (mods.isNotEmpty()) {
      appendLine("      modifierExpr(node)?.let { sb.append(\"\${indent}  modifier = \$it,\\n\") }")
    }
    for (p in w.props) {
      if (p.required) {
        appendLine("      sb.append(\"\${indent}  ${p.name} = \${${literalExpr(p)}},\\n\")")
      } else {
        appendLine("      if (\"${p.name}\" in node.props) sb.append(\"\${indent}  ${p.name} = \${${literalExpr(p)}},\\n\")")
      }
    }
    for (e in w.events) {
      appendLine("      (node.props[\"${e.name}\"] as? Action)?.let { a -> sb.append(\"\${indent}  ${e.name} = \${fmtAction(a, ${e.paramCount})},\\n\") }")
    }
    if (w.hasChildren) {
      appendLine("      sb.append(\"\$indent) {\\n\")")
      appendLine("      node.children.forEach { emitNode(sb, it, \"\$indent  \") }")
      appendLine("      sb.append(\"\$indent}\\n\")")
    } else {
      appendLine("      sb.append(\"\$indent)\\n\")")
    }
    appendLine("    }")
  }
  appendLine("    else -> sb.append(\"\$indent// unknown widget: \${node.type}\\n\")")
  appendLine("  }")
  appendLine("}")
}
